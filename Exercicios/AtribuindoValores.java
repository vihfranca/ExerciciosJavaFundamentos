package Exercicios;

public class AtribuindoValores {
    public static void main(String[] args) {

        int idade = 20;
        double quantidade = 15.5;
        char feminino = 'F';
        boolean menorQuantidade = true;

        System.out.println("###########################");
        System.out.println("#    Atribuindo Valores   #");
        System.out.println("###########################");

        System.out.println();

        System.out.println("double quantidade = " + quantidade);
        System.out.println("boolean menorQuantidade = " + menorQuantidade);
        System.out.println("int idade = " + idade);
        System.out.println("char feminino = " + feminino);

        System.out.println();
        System.out.print("###########################");
    }
}
